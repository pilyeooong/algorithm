def get_count_of_departments_cleaned_by_robot_vacuum(r, c, d, room_map):
    answer = 0

    row = len(room_map)
    col = len(room_map[0])

    visited = [[False] * col for _ in range(row)]

    q = deque()
    q.append((r, c, d))
    visited[r][c] = True
    answer += 1

    # 회전 시 변경 방향
    turn = {
        0: 3, # 북
        1: 0, # 동
        2: 1, # 서
        3: 2  # 남
    }

    # 후진 시 변경 방향
    back = {
        0: 2,
        1: 3,
        2: 0,
        3: 1
    }
    directions = [(-1, 0), (0, 1), (1, 0), (0, -1)]

    while q:
        x, y, cur_d = q.popleft()
        for i in range(4):
            cur_d = turn[cur_d]
            dx, dy = directions[cur_d]
            next_x, next_y = x + dx, y + dy

            if next_x >= 0 and next_x < row and next_y >= 0 and next_y < col and visited[next_x][next_y] == False and room_map[next_x][next_y] == 0:
                q.append((next_x, next_y, cur_d))
                visited[next_x][next_y] = True
                answer += 1
                break
            elif i == 3:
                back_d = back[cur_d]
                dx, dy = directions[back_d]
                back_x, back_y = x + dx, y + dy
                q.append((back_x, back_y, cur_d))

                if room_map[back_x][back_y] == 1:
                    return answer

    return answer


