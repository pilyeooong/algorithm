def get_minimum_count_of_overseas_supply(stock, dates, supplies, k):
    last_idx = 0
    answer = 0
    heap_supplies = []

    while stock <= k:
        while last_idx < len(dates) and dates[last_idx] <= stock:
            supply = supplies[last_idx]
            heapq.heappush(heap_supplies, -supply)
            last_idx += 1

        heap_supply = heapq.heappop(heap_supplies)
        stock += -heap_supply
        answer += 1

    return answer


